package and.hodz.algorithms.linked_list.swap_nodes_in_pair;

public class Main {
    public static void main(String[] args) {

        int i = 0;
        while (i < 10) {
            if (i % 2 == 0)
                System.out.println(i);
            else {
                i++;
                continue;
            }
            i++;
        }
    }

}
